// postfix increment

let x = 10;
let c = x++;
let y = x++;
console.log(x, y, c);

// prefix increment

let p = 10;
let d = ++p;
let r = ++p;
console.log(p, r, d);

// postfix decrement
let s = 10;
let e = s--;
let t = s--;
console.log(s, t, e);

// prefix decrement
let j = 10;
let g = --j;
let o = --j;
console.log(j, o, g);
